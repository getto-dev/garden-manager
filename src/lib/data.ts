export function generateCalendarMonth(year: number, month: number): MoonDay[] {
  const days: MoonDay[] = [];
  const daysInMonth = new Date(year, month, 0).getDate();

  for (let day = 1; day <= daysInMonth; day++) {
    const date = new Date(year, month - 1, day);
    const moonInfo = calculateMoonPhase(date);
    
    days.push({
      id: `${year}-${month}-${day}`,
      date: `${year}-${String(month).padStart(2, '0')}-${String(day).padStart(2, '0')}`,
      year,
      month,
      day,
      moonPhase: moonInfo.phase,
      moonPhaseName: moonInfo.phaseName,
      moonDay: moonInfo.moonDay,
      zodiacSign: moonInfo.zodiacSign,
      zodiacElement: moonInfo.zodiacElement,
      isGoodForSowing: moonInfo.isGoodForSowing,
      isGoodForPlanting: moonInfo.isGoodForPlanting,
      isGoodForTransplanting: moonInfo.isGoodForTransplanting,
      isGoodForWatering: moonInfo.isGoodForWatering,
      isGoodForFertilizing: moonInfo.isGoodForFertilizing,
      isGoodForPruning: moonInfo.isGoodForPruning,
      isGoodForHarvesting: moonInfo.isGoodForHarvesting,
      isGoodForSoilWork: moonInfo.isGoodForSoilWork,
      isForbidden: moonInfo.isForbidden,
      recommendation: moonInfo.recommendation
    });
  }

  return days;
}

// Расчёт фазы луны
function calculateMoonPhase(date: Date): MoonDayInfo {
  // Базовая дата новолуния (6 января 2000 года)
  const baseNewMoon = new Date(2000, 0, 6, 18, 14, 0);
  const lunarCycle = 29.530588853; // Длина синодического месяца в днях
  
  const daysSinceBase = (date.getTime() - baseNewMoon.getTime()) / (1000 * 60 * 60 * 24);
  const moonAge = ((daysSinceBase % lunarCycle) + lunarCycle) % lunarCycle;
  const moonDay = Math.floor(moonAge) + 1;

  // Определение фазы
  let phase: string;
  let phaseName: string;
  let isGoodForSowing = false;
  let isGoodForPlanting = false;
  let isGoodForTransplanting = false;
  let isGoodForWatering = false;
  let isGoodForFertilizing = false;
  let isGoodForPruning = false;
  let isGoodForHarvesting = false;
  let isGoodForSoilWork = false;
  let isForbidden = false;
  let recommendation = '';

  if (moonAge < 1.85) {
    phase = 'new';
    phaseName = 'Новолуние';
    isForbidden = true;
    recommendation = 'Не рекомендуется ничего сажать и пересаживать. Хорошее время для планирования и подготовки.';
  } else if (moonAge < 7.38) {
    phase = 'waxing_crescent';
    phaseName = 'Молодая луна';
    isGoodForSowing = true;
    isGoodForPlanting = true;
    recommendation = 'Благоприятное время для посева и посадки растений надземной части: томаты, огурцы, перец, капуста, зелень.';
  } else if (moonAge < 9.23) {
    phase = 'first_quarter';
    phaseName = 'Первая четверть';
    isGoodForPlanting = true;
    isGoodForTransplanting = true;
    isGoodForWatering = true;
    recommendation = 'Хорошее время для пересадки и полива. Подкормка растений надземной части.';
  } else if (moonAge < 14.77) {
    phase = 'waxing_gibbous';
    phaseName = 'Прибывающая луна';
    isGoodForSowing = true;
    isGoodForPlanting = true;
    isGoodForFertilizing = true;
    recommendation = 'Отличное время для посадки плодовых культур, подкормок. Активный рост растений.';
  } else if (moonAge < 16.61) {
    phase = 'full';
    phaseName = 'Полнолуние';
    isGoodForHarvesting = true;
    isGoodForPruning = true;
    recommendation = 'Время сбора урожая, заготовки семян. Не сажать и не пересаживать. Можно проводить обрезку.';
  } else if (moonAge < 22.15) {
    phase = 'waning_gibbous';
    phaseName = 'Убывающая луна';
    isGoodForTransplanting = true;
    isGoodForSoilWork = true;
    recommendation = 'Благоприятно для корнеплодов: морковь, свёкла, картофель. Пересадка, работа с почвой.';
  } else if (moonAge < 24.00) {
    phase = 'last_quarter';
    phaseName = 'Последняя четверть';
    isGoodForPruning = true;
    isGoodForSoilWork = true;
    recommendation = 'Время для обрезки, борьбы с вредителями, работы с почвой.';
  } else {
    phase = 'waning_crescent';
    phaseName = 'Старая луна';
    isGoodForSoilWork = true;
    recommendation = 'Подготовка почвы, борьба с сорняками и вредителями. Отдых растений.';
  }

  // Определение знака зодиака (упрощённое)
  const zodiacInfo = getZodiacSign(date);

  return {
    phase,
    phaseName,
    moonDay,
    zodiacSign: zodiacInfo.sign,
    zodiacElement: zodiacInfo.element,
    isGoodForSowing,
    isGoodForPlanting,
    isGoodForTransplanting,
    isGoodForWatering,
    isGoodForFertilizing,
    isGoodForPruning,
    isGoodForHarvesting,
    isGoodForSoilWork,
    isForbidden,
    recommendation
  };
}

// Определение знака зодиака
function getZodiacSign(date: Date): { sign: string; element: string } {
  const month = date.getMonth() + 1;
  const day = date.getDate();

  const signs: { sign: string; element: string; start: [number, number]; end: [number, number] }[] = [
    { sign: '♈ Овен', element: 'Огонь', start: [3, 21], end: [4, 19] },
    { sign: '♉ Телец', element: 'Земля', start: [4, 20], end: [5, 20] },
    { sign: '♊ Близнецы', element: 'Воздух', start: [5, 21], end: [6, 20] },
    { sign: '♋ Рак', element: 'Вода', start: [6, 21], end: [7, 22] },
    { sign: '♌ Лев', element: 'Огонь', start: [7, 23], end: [8, 22] },
    { sign: '♍ Дева', element: 'Земля', start: [8, 23], end: [9, 22] },
    { sign: '♎ Весы', element: 'Воздух', start: [9, 23], end: [10, 22] },
    { sign: '♏ Скорпион', element: 'Вода', start: [10, 23], end: [11, 21] },
    { sign: '♐ Стрелец', element: 'Огонь', start: [11, 22], end: [12, 21] },
    { sign: '♑ Козерог', element: 'Земля', start: [12, 22], end: [1, 19] },
    { sign: '♒ Водолей', element: 'Воздух', start: [1, 20], end: [2, 18] },
    { sign: '♓ Рыбы', element: 'Вода', start: [2, 19], end: [3, 20] }
  ];

  for (const s of signs) {
    const [startMonth, startDay] = s.start;
    const [endMonth, endDay] = s.end;

    if (startMonth === 12 && endMonth === 1) {
      if ((month === 12 && day >= startDay) || (month === 1 && day <= endDay)) {
        return { sign: s.sign, element: s.element };
      }
    } else if (
      (month === startMonth && day >= startDay) ||
      (month === endMonth && day <= endDay)
    ) {
      return { sign: s.sign, element: s.element };
    }
  }

  return { sign: '♈ Овен', element: 'Огонь' };
}

